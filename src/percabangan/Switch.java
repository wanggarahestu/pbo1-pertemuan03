package percabangan;

public class Switch {
    public static void main(String[] args) {
        int jalanJalan=3;
        switch (jalanJalan){
            case 1:
                System.out.println("Urang Lapas");
                break;
            case 2:
                System.out.println("Urang Acayaan");
                break;
            case 3:
                System.out.println("Ubat Nyamuk");
                break;
            case 4:
                System.out.println("Dubil Dit");
                break;
                default:
                    System.out.println("Mahluk Halus");
                    break;


        }
    }

}